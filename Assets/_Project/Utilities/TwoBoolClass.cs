namespace _Project.Utilities
{
    public class TwoBoolClass
    {
        public bool Bool1 { get; set; }
        public bool Bool2 { get; set; }

        public TwoBoolClass()
        {
            
        }
        public TwoBoolClass(bool bool1, bool bool2)
        {
            Bool1 = bool1;
            Bool2 = bool2;
        }
    }
}